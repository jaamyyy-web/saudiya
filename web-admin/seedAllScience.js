function generateChapterQuestions(chapterNum, title, concepts) {
  const mcqs = [];
  const fibs = [];
  const tfs = [];
  const hoqs = [];

  // Define concepts to expand into questions
  const c = concepts || [
    { q: 'الفكرة الأساسية للدرس', o: ['التعلم والتطبيق', 'الحفظ فقط', 'التجاهل', 'عدم الدراسة'], a: 0, ex: 'الهدف هو الفهم أولاً ثم التطبيق.' },
    { q: 'أهمية هذه الوحدة', o: ['تفسير الظواهر الطبيعية', 'عدم الاهتمام بالكون', 'الحفظ السريع', 'النجاح بدون دراسة'], a: 0, ex: 'تساعدنا العلوم على فهم الكون والظواهر المحيطة بنا.' }
  ];

  // 1. Generate 15 MCQs
  for (let i = 0; i < 15; i++) {
    const concept = c[i % c.length];
    mcqs.push({
      type: 'MCQ',
      question: `[سؤال اختبار] ${concept.q} (السؤال الفرعي ${i + 1})؟`,
      options: concept.o || ['الخيار الصحيح', 'الخيار الثاني', 'الخيار الثالث', 'الخيار الرابع'],
      answerIndex: concept.a || 0,
      explanation: concept.ex || 'الشرح العلمي الصحيح يساعد الطالب على فهم الفكرة.',
      difficulty: i < 5 ? 'easy' : i < 10 ? 'medium' : 'hard'
    });
  }

  // 2. Generate 10 FIBs
  for (let i = 0; i < 10; i++) {
    const concept = c[(i + 2) % c.length];
    fibs.push({
      type: 'FIB',
      question: `يسمى المفهوم العلمي الذي يعبر عن ${concept.q.replace('ما هو ', '').replace('ما هي ', '')} بـ _______ .`,
      options: [concept.o[0], concept.o[1], concept.o[2], concept.o[3]],
      answerIndex: 0,
      explanation: concept.ex || 'الشرح العلمي الصحيح يثبت فهم الطالب للمصطلح.',
      difficulty: i < 4 ? 'easy' : i < 8 ? 'medium' : 'hard'
    });
  }

  // 3. Generate 10 TFs
  for (let i = 0; i < 10; i++) {
    const concept = c[(i + 4) % c.length];
    const isTrue = i % 2 === 0;
    tfs.push({
      type: 'TF',
      question: isTrue 
        ? `يعتبر ${concept.o[0]} هو التفسير أو التوصيف الصحيح لـ ${concept.q.replace('ما هو ', '').replace('ما هي ', '')}.`
        : `يعتبر ${concept.o[1]} هو التوصيف الإيجابي الوحيد لـ ${concept.q.replace('ما هو ', '').replace('ما هي ', '')}.`,
      options: ['صح', 'خطأ'],
      answerIndex: isTrue ? 0 : 1,
      explanation: concept.ex || 'الشرح والتعليل يوضحان الفكرة العلمية للطالب.',
      difficulty: i < 4 ? 'easy' : i < 8 ? 'medium' : 'hard'
    });
  }

  // 4. Generate 3 HOQs
  for (let i = 0; i < 3; i++) {
    const concept = c[(i + 6) % c.length];
    hoqs.push({
      type: 'HOQ',
      question: `[تفكير ناقد] ما الاستنتاج العلمي المترتب على ${concept.q.replace('ما هو ', '').replace('ما هي ', '')}؟`,
      options: [
        `يؤدي إلى ${concept.o[0]} وتكامل العمليات الحيوية والفيزيائية المترتبة`,
        `يسبب ${concept.o[1]} وتوقف تام للظواهر الطبيعية الموصوفة`,
        `يؤدي إلى ${concept.o[2]} بشكل عشوائي دون أي نظام`,
        `يتسبب في ${concept.o[3]} المباشر`
      ],
      answerIndex: 0,
      explanation: `أسئلة التفكير العليا تقيس مهارات التحليل والاستنتاج وحل المشكلات لدى الطالب: ${concept.ex}`,
      difficulty: 'hard'
    });
  }

  return [...mcqs, ...fibs, ...tfs, ...hoqs];
}

