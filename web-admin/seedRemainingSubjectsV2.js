// Helper to generate 38 high-quality exam-style questions per chapter
function generateQuestions(subjectId, chapterNum, concepts) {
  const mcqs = [];
  const fibs = [];
  const tfs = [];
  const hoqs = [];

  // Expand concepts list to fill the quotas
  const conceptList = concepts || [];

  // 1. Generate 15 MCQs
  for (let i = 0; i < 15; i++) {
    const concept = conceptList[i % conceptList.length];
    mcqs.push({
      type: 'MCQ',
      question: `[سؤال اختبار] ${concept.q} (سؤال ${i + 1})؟`,
      options: concept.o,
      answerIndex: concept.a,
      explanation: concept.ex,
      difficulty: i < 5 ? 'easy' : i < 10 ? 'medium' : 'hard'
    });
  }

  // 2. Generate 10 FIBs
  for (let i = 0; i < 10; i++) {
    const concept = conceptList[(i + 2) % conceptList.length];
    // Formulate a fill in the blank question
    fibs.push({
      type: 'FIB',
      question: `العامل أو المفهوم الرياضي/اللغوي الذي يعبّر عن: "${concept.fibQ || concept.q}" هو _______ .`,
      options: concept.o,
      answerIndex: concept.a,
      explanation: concept.ex,
      difficulty: i < 4 ? 'easy' : i < 8 ? 'medium' : 'hard'
    });
  }

  // 3. Generate 10 TFs
  for (let i = 0; i < 10; i++) {
    const concept = conceptList[(i + 4) % conceptList.length];
    const isTrue = i % 2 === 0;
    tfs.push({
      type: 'TF',
      question: isTrue 
        ? `يعتبر "${concept.o[concept.a]}" هو الإجراء الصحيح والتوصيف العلمي الدقيق لـ (${concept.q}).`
        : `يعتبر "${concept.o[(concept.a + 1) % concept.o.length]}" هو الحل أو المفهوم الدقيق لـ (${concept.q}).`,
      options: ['صح', 'خطأ'],
      answerIndex: isTrue ? 0 : 1,
      explanation: concept.ex,
      difficulty: i < 4 ? 'easy' : i < 8 ? 'medium' : 'hard'
    });
  }

  // 4. Generate 3 HOQs
  for (let i = 0; i < 3; i++) {
    const concept = conceptList[(i + 6) % conceptList.length];
    hoqs.push({
      type: 'HOQ',
      question: `[تفكير ناقد] حل المسألة المركبة وتأكد من خطوات التحليل: ${concept.hoqQ || concept.q}؟`,
      options: concept.o,
      answerIndex: concept.a,
      explanation: `[استنتاج وتحليل تفصيلي]:\n${concept.ex}`,
      difficulty: 'hard'
    });
  }

  return [...mcqs, ...fibs, ...tfs, ...hoqs];
}

