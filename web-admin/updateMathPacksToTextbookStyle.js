async function main() {
  try {
    console.log('Logging in anonymously to Firestore...');
    const cred = await signInAnonymously(auth);
    console.log('Authenticated successfully! UID:', cred.user.uid);

    console.log('\n=========================================');
    console.log('STARTING UPDATES TO PREMIUM TEXTBOOK STYLE FOR: الرياضيات (9 CHAPTERS)');
    console.log('=========================================');

    // 1. Get all learning packs under 'math'
    const packsQuery = query(collection(db, 'learning_packs'), where('subjectId', '==', 'math'));
    const querySnapshot = await getDocs(packsQuery);
    
    console.log(`Found ${querySnapshot.size} math learning packs in the database.`);

    for (const packDoc of querySnapshot.docs) {
      const packData = packDoc.data();
      const chNum = packData.order; // Using 'order' to map the chapter number
      const matchingCh = mathChapters.find(c => c.num === chNum);

      if (matchingCh) {
        console.log(`\n--- Updating Math Chapter ${chNum}: ${matchingCh.title} ---`);
        
        // Update Learning Pack Title
        console.log('1. Updating Learning Pack Title...');
        await updateDoc(doc(db, 'learning_packs', packDoc.id), {
          title: matchingCh.title,
          updatedAt: serverTimestamp()
        });

        // 2. Query and update the matching summary
        console.log('2. Updating Summary details...');
        const summariesQuery = query(collection(db, 'summaries'), where('packId', '==', packDoc.id));
        const summarySnapshot = await getDocs(summariesQuery);
        for (const sDoc of summarySnapshot.docs) {
          await updateDoc(doc(db, 'summaries', sDoc.id), {
            title: matchingCh.summary.title,
            body: matchingCh.summary.body,
            points: matchingCh.summary.points,
            updatedAt: serverTimestamp()
          });
        }
        console.log('   Summary successfully updated!');

        // 3. Query and update all 38 questions associated with this pack
        console.log('3. Updating Questions to clean premium step-by-step styles...');
        const questionsQuery = query(collection(db, 'questions'), where('packId', '==', packDoc.id));
        const questionsSnapshot = await getDocs(questionsQuery);
        
        console.log(`   Found ${questionsSnapshot.size} questions for this pack.`);
        
        // Re-generate list of questions in identical order for replacement
        const generateQuestionsList = (chapter) => {
          const mcqs = [];
          const fibs = [];
          const tfs = [];
          const hoqs = [];
          const conceptList = chapter.concepts;

          // 15 MCQ
          for (let i = 0; i < 15; i++) {
            const concept = conceptList[i % conceptList.length];
            mcqs.push({
              type: 'MCQ',
              question: `[سؤال اختبار] ${concept.q} (سؤال ${i + 1})`,
              options: concept.o,
              answerIndex: concept.a,
              explanation: concept.ex,
            });
          }

          // 10 FIB
          for (let i = 0; i < 10; i++) {
            const concept = conceptList[(i + 2) % conceptList.length];
            fibs.push({
              type: 'FIB',
              question: `أكمل الجملة الرياضية التالية بما يناسب: "${concept.q}" هو _______ .`,
              options: concept.o,
              answerIndex: concept.a,
              explanation: concept.ex,
            });
          }

          // 10 TF
          for (let i = 0; i < 10; i++) {
            const concept = conceptList[(i + 4) % conceptList.length];
            const isTrue = i % 2 === 0;
            const tfQuestion = isTrue 
              ? `تعتبر العبارة التالية: "${concept.o[concept.a]} هو الحل الصحيح والنهائي لـ (${concept.q})"`
              : `تعتبر العبارة التالية: "${concept.o[(concept.a + 1) % concept.o.length]} هو التبسيط أو المفهوم الدقيق لـ (${concept.q})"`;

            tfs.push({
              type: 'TF',
              question: tfQuestion,
              options: ['صح', 'خطأ'],
              answerIndex: isTrue ? 0 : 1,
              explanation: concept.ex,
            });
          }

          // 3 HOQ
          for (let i = 0; i < 3; i++) {
            const concept = conceptList[(i + 6) % conceptList.length];
            hoqs.push({
              type: 'HOQ',
              question: `[تفكير ناقد] حل المسألة المركبة وتأكد من خطوات التحليل: ${concept.q}؟`,
              options: concept.o,
              answerIndex: concept.a,
              explanation: `[تحليل واستنتاج تفصيلي]:\n${concept.ex}`,
            });
          }

          return [...mcqs, ...fibs, ...tfs, ...hoqs];
        };

        const replacementQuestions = generateQuestionsList(matchingCh);

        // Map and update each question based on its order field
        for (const qDoc of questionsSnapshot.docs) {
          const qData = qDoc.data();
          const qOrder = qData.order; // 1-indexed
          const replacement = replacementQuestions[qOrder - 1];

          if (replacement) {
            await updateDoc(doc(db, 'questions', qDoc.id), {
              question: replacement.question,
              options: replacement.options,
              answerIndex: replacement.answerIndex,
              explanation: replacement.explanation,
              updatedAt: serverTimestamp()
            });
          }
        }
        console.log(`   Successfully updated all 38 questions for Math Chapter ${chNum}!`);
      }
    }

    console.log('\n🎉 ALL MATH PACKS SUCCESSFULLY CONVERTED TO TEXTBOOK STYLE & DOCK ACTIVE!');
    process.exit(0);
  } catch (error) {
    console.error('Error during Math update script execution:', error);
    process.exit(1);
  }
}

main();
