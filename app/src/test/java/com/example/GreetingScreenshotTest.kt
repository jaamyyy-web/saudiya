import androidx.compose.ui.test.onRoot
import com.example.ui.theme.MyApplicationTheme
import com.github.takahirom.roborazzi.RobolectricDeviceQualifiers
import com.github.takahirom.roborazzi.captureRoboImage
import org.junit.Rule
import org.junit.Test
import org.junit.runner.RunWith
import org.robolectric.RobolectricTestRunner
import org.robolectric.annotation.Config
import org.robolectric.annotation.GraphicsMode

import androidx.navigation.compose.rememberNavController

@RunWith(RobolectricTestRunner::class)
@GraphicsMode(GraphicsMode.Mode.NATIVE)
@Config(qualifiers = RobolectricDeviceQualifiers.Pixel8, sdk = [36])
class GreetingScreenshotTest {

  @get:Rule val composeTestRule = createComposeRule()

  @Test
  fun student_home_screenshot() {
    composeTestRule.setContent { MyApplicationTheme { StudentHomeScreen(rememberNavController()) } }

    composeTestRule.onRoot().captureRoboImage(filePath = "src/test/screenshots/student_home.png") // Note: Due to screenshot tests this might generate differently with JetBrains coroutines or other dependencies.
  }
}
